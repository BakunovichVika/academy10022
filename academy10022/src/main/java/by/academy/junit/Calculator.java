package by.academy.junit;

public class Calculator {

        public static int sum(int x, int y) {
            return x + y;
        }

        public static int divide(int x, int y) {
            return x / y;
        }

        public static int multiple(int x, int y) {
            return x * y;
        }

        public static int substract(int x, int y) {
            return x - y;
        }
    }